    private void initialiser()
    {
        if (donnees.length > 0 )
        {
            op = "M";
            int i = 0;
            jTextFieldCodeCategorie.setText(Res.ft_Chaine(donnees[i++]));
            jTextFieldLibelle.setText(Res.ft_Chaine(donnees[i++]));
        }
        else
        {
            op = "A";
            jTextFieldCodeCategorie.setText("");
            jTextFieldLibelle.setText("");
        }
        ancienCode = jTextFieldCodeCategorie.getText();
    }
    
    
    private void ajouter(){
        String[] parametre={jTextFieldCodeCategorie.getText(),jTextFieldLibelle.getText()};
        Paquet result=Ctrl.ajouter(parametre);
        if (result.getOk()){
            initialiser();
        }
    }
    
    private void modifier(){
        Object[] parametre={jTextFieldCodeCategorie.getText(),jTextFieldLibelle.getText(), ancienCode};
        Paquet result=Ctrl.modifier(parametre);
        if (result.getOk()){
            initialiser();
        }
    }

